"""
notes_to_tiles.py

Converts a list of (freq, duration) notes extracted from MIDI
into a tile JSON for the piano tiles game.

- t       = when the tile appears (seconds from start)
- row     = 0-5 based on frequency bucket
- width   = proportional to note duration (scaled to tile units)

Usage:
    python notes_to_tiles.py
    (edit the `notes` list at the bottom, output saved to tiles.json)
"""

