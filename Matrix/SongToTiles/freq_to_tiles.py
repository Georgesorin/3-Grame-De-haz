# ── Config ────────────────────────────────────────────────────────────────────
OUTPUT_FILE    = r"C:\Users\bmdma\ledhack\3-Grame-De-haz\Matrix\songs\song.json"
NUM_ROWS       = 6       # rows in the game (0–5)
MIN_WIDTH      = 2       # shortest tile width
MAX_WIDTH      = 8       # longest tile width
MIN_GAP        = 0.05    # minimum seconds of silence between tiles
                         # (set to 0 to let tiles touch end-to-end)
# ─────────────────────────────────────────────────────────────────────────────


def freq_to_row(freq, min_f, max_f):
    """Map a frequency to a row 0–5 using equal-width buckets."""
    if min_f == max_f:
        return 0
    bucket = (max_f - min_f) / NUM_ROWS
    row = int((freq - min_f) / bucket)
    return min(row, NUM_ROWS - 1)


def dur_to_width(dur, min_dur, max_dur):
    """Map a duration to a tile width between MIN_WIDTH and MAX_WIDTH."""
    if min_dur == max_dur:
        return MIN_WIDTH
    # linear scale
    ratio = (dur - min_dur) / (max_dur - min_dur)
    width = MIN_WIDTH + ratio * (MAX_WIDTH - MIN_WIDTH)
    return max(MIN_WIDTH, min(MAX_WIDTH, round(width)))


def notes_to_tiles(notes):
    """
    notes: list of (freq, duration_seconds)
    Returns a list of tile dicts.
    """
    freqs = [f for f, d in notes]
    durs  = [d for f, d in notes]

    min_f   = min(freqs)
    max_f   = max(freqs)
    min_dur = min(durs)
    max_dur = max(durs)

    tiles   = []
    cursor  = 0.0   # current time position in seconds

    for freq, dur in notes:
        t     = round(cursor, 3)
        row   = freq_to_row(freq, min_f, max_f)
        width = dur_to_width(dur, min_dur, max_dur)

        tiles.append({"t": t, "row": row, "width": width})

        # next tile starts right after this one ends (+ tiny gap)
        cursor += dur + MIN_GAP

    return tiles


def build_json(tiles):
    return {
        "format": 1,
        "comment": (
            "t = seconds from round start. "
            "row 0-5 mapped from frequency range (0=lowest, 5=highest). "
            "width proportional to note duration."
        ),
        "tiles": tiles,
    }


